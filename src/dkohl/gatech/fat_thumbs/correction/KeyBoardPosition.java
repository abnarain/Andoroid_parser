package dkohl.gatech.fat_thumbs.correction;

public class KeyBoardPosition {
	private int row;
	private int col;

	public KeyBoardPosition(int row, int col) {
		this.row = row;
		this.col = col;
	}

	public int getRow() {
		return row;
	}

	public int getCol() {
		return col;
	}

}
